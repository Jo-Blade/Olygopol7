/** Vecteur de deux flottants.
 * @author : pisento
**/

public class FloatVec3 {

  /** Première coordonnée du vecteur.*/
  final public float x;

  /** Deuxième coordonnée du vecteur.*/
  final public float y;

  /** Troisième coordonnée du vecteur.*/
  final public float z;

  /** Créer un nouveau vecteur avec ses coordonnées.
   * @param x coordonnée 1
   * @param y coordonnée 2
   * @param z coordonnée 3
   */
  public FloatVec3(float x, float y, float z) {
    this.x = x;
    this.y = y;
    this.z = z;
  }

}
